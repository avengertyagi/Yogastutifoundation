                  <p class="text-start fs-6">Become aware of the flow of your breath.
                     Relaxing your abdomen and the sides of your rib cage, focus on the
                      sensations of inhaling and exhaling. Allow the breath to become deep
                       and diaphragmatic. Breathing becomes calm and natural, not forced. 
                       When you are aware of your breath for an extended period of time,
                        you’ll notice the breath naturally starts to deepen and become more smooth.
                         This relaxes your mind and nervous system in preparation for turning inward in meditation.
                         Your mind will start to calm down, and distracting thoughts will take a back seat to the
                          gentle flow of breath in and out.</p>
                          <h2 class="fw-bold fs-4 text-start">3.Breath Awareness in the Nostrils</h2>
                          <p class="text-start fs-6">This is the beginning of formal concentration practice.
                             The sensation of the breath in the nostrils is a calming focus that will make your meditation
                              stable and grounded. Breath awareness in the nostrils connects you to the core energies of your body
                               and mind, which creates a deep sense of quietness and joy. As you inhale, feel the breath entering
                                the opening of your nostrils.</p>
